<h3 class="mg-md">KeyChest Enterprise</h3>

<p>
    The free version of KeyChest is a perfect fit for everyone who cares about the security of their servers. While Let's
    Encrypt provides free certificates, we decided to provide you a free monitoring service so that you never regret the
    day you secured your website. Just because your certificate expired and made your website unavailable.
</p>

<p>
    Still, there are users who need more as their operations are more complex. This requires more effort from our side - not only to develop
    new features, but also to support these users. Security can get complicated when used in an environment with a number of
    internal networks, offices, or even internal key management. We work hard to help even these users and here is a list
    of features we expect to launch in August/September.
</p>

<h3 class="mg-md">Enterprise Options</h3>

<ul>
    <li>User/role management - ability to define departments and users assigned to the departments.</li>
    <li>Internal networks - monitoring of certificates in internal networks (or VLANs) requiring local agents.</li>
    <li>Internal key management - ability to verify certificates issued by internal CAs/PKI systems.</li>
</ul>

